func strPtr(s string) *string { return &s }

func TestGenerateCreateTable(t *testing.T) {
	d := dialect{}
	got, err := d.GenerateCreateTable(dbdriver.TableSchema{
		Name:   "users",
		Schema: "public",
		Columns: []dbdriver.ColumnMeta{
			{Name: "id", NativeType: "BIGINT", IsPrimaryKey: true, IsAutoIncrement: true},
			{Name: "name", NativeType: "VARCHAR(64)", Nullable: false},
			{Name: "note", NativeType: "TEXT", Nullable: true, Comment: "free text"},
			{Name: "created_at", NativeType: "TIMESTAMPTZ", Nullable: false, Default: strPtr("now()")},
		},
		Indexes: []dbdriver.IndexInfo{
			{Name: "users_pkey", Primary: true, Columns: []dbdriver.IndexColumn{{Name: "id"}}},
			{Name: "ix_name", Unique: true, Type: "BTREE", Columns: []dbdriver.IndexColumn{{Name: "name", Order: "DESC"}}},
			{Name: "ix_note", Type: "GIN", Columns: []dbdriver.IndexColumn{{Name: "note"}}},
		},
		ForeignKeys: []dbdriver.ForeignKeyInfo{
			{Name: "fk_org", Columns: []string{"org_id"}, ReferencedSchema: "public",
				ReferencedTable: "orgs", ReferencedColumns: []string{"id"}, OnDelete: "CASCADE"},
		},
		Comment: "app users",
	})
	if err != nil {
		t.Fatalf("GenerateCreateTable: %v", err)
	}
	wantFragments := []string{
		`CREATE TABLE "public"."users" (`,
		`"id" BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL`,
		`"name" VARCHAR(64) NOT NULL`,
		`"note" TEXT NULL`,
		`"created_at" TIMESTAMPTZ NOT NULL DEFAULT now()`,
		`PRIMARY KEY ("id")`,
		`CONSTRAINT "fk_org" FOREIGN KEY ("org_id") REFERENCES "public"."orgs" ("id") ON DELETE CASCADE`,
		`CREATE UNIQUE INDEX "ix_name" ON "public"."users" ("name" DESC);`,
		`CREATE INDEX "ix_note" ON "public"."users" USING gin ("note");`,
		`COMMENT ON TABLE "public"."users" IS 'app users';`,
		`COMMENT ON COLUMN "public"."users"."note" IS 'free text';`,
	}
	for _, w := range wantFragments {
		if !strings.Contains(got, w) {
			t.Errorf("missing fragment %q in:\n%s", w, got)
		}
	}
	// The primary index must not become a duplicate CREATE INDEX.
	if strings.Contains(got, "users_pkey") {
		t.Errorf("primary index leaked into CREATE INDEX:\n%s", got)
	}
	// No inline COMMENT clause — Postgres has none.
	if strings.Contains(got, "NULL COMMENT") || strings.Contains(got, `TEXT NULL 'free text'`) {
		t.Errorf("inline comment leaked into column def:\n%s", got)
	}
}

func TestGenerateCreateTableSerialPassthrough(t *testing.T) {
	d := dialect{}
	got, err := d.GenerateCreateTable(dbdriver.TableSchema{
		Name: "t",
		Columns: []dbdriver.ColumnMeta{
			{Name: "id", NativeType: "SERIAL", IsPrimaryKey: true, IsAutoIncrement: true},
		},
	})
	if err != nil {
		t.Fatalf("GenerateCreateTable: %v", err)
	}
	if strings.Contains(got, "IDENTITY") {
		t.Errorf("SERIAL column must not get an extra IDENTITY clause:\n%s", got)
	}
}

