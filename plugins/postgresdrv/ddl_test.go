func TestGenerateAlterTableColumns(t *testing.T) {
	d := dialect{}
	stmts, err := d.GenerateAlterTable("app", "public", "users", dbdriver.ChangeSet{
		Columns: []dbdriver.ColumnChange{
			{Kind: dbdriver.ColumnDrop, Name: "legacy"},
			{Kind: dbdriver.ColumnAdd, Column: &dbdriver.ColumnMeta{Name: "age", NativeType: "INTEGER", Nullable: true}},
			{Kind: dbdriver.ColumnModify, Column: &dbdriver.ColumnMeta{Name: "name", NativeType: "VARCHAR(128)", Nullable: false}},
			{Kind: dbdriver.ColumnRename, Name: "old", Column: &dbdriver.ColumnMeta{Name: "new", NativeType: "TEXT", Nullable: true}},
		},
	})
	if err != nil {
		t.Fatalf("GenerateAlterTable: %v", err)
	}
	joined := strings.Join(stmts, "\n")
	wantFragments := []string{
		`ALTER TABLE "public"."users" DROP COLUMN "legacy";`,
		`ALTER TABLE "public"."users" ADD COLUMN "age" INTEGER NULL;`,
		`ALTER TABLE "public"."users" ALTER COLUMN "name" TYPE VARCHAR(128) USING "name"::VARCHAR(128);`,
		`ALTER TABLE "public"."users" ALTER COLUMN "name" SET NOT NULL;`,
		`ALTER TABLE "public"."users" RENAME COLUMN "old" TO "new";`,
		`ALTER TABLE "public"."users" ALTER COLUMN "new" DROP NOT NULL;`,
	}
	for _, w := range wantFragments {
		if !strings.Contains(joined, w) {
			t.Errorf("missing statement %q in:\n%s", w, joined)
		}
	}
}

func TestGenerateAlterTablePKIndexFKComment(t *testing.T) {
	d := dialect{}
	stmts, err := d.GenerateAlterTable("app", "public", "users", dbdriver.ChangeSet{
		PrimaryKey: &dbdriver.PrimaryKeyChange{Drop: true, Columns: []string{"id", "tenant"}},
		Indexes: []dbdriver.IndexChange{
			{Kind: "drop", Name: "ix_old"},
			{Kind: "add", Index: &dbdriver.IndexInfo{Name: "ix_new", Columns: []dbdriver.IndexColumn{{Name: "a"}}, Type: "HASH"}},
		},
		ForeignKeys: []dbdriver.ForeignKeyChange{
			{Kind: "drop", Name: "fk_old"},
			{Kind: "add", ForeignKey: &dbdriver.ForeignKeyInfo{
				Name: "fk_new", Columns: []string{"b"},
				ReferencedSchema: "public", ReferencedTable: "p", ReferencedColumns: []string{"id"},
				OnUpdate: "RESTRICT",
			}},
		},
		Options: []dbdriver.TableOptionChange{{Name: "comment", Value: "hi"}},
	})
	if err != nil {
		t.Fatalf("GenerateAlterTable: %v", err)
	}
	joined := strings.Join(stmts, "\n")
	wantFragments := []string{
		`contype = 'p'`, // DO-block PK drop resolves the constraint name server-side
		`ALTER TABLE "public"."users" ADD PRIMARY KEY ("id", "tenant");`,
		`DROP INDEX "public"."ix_old";`,
		`CREATE INDEX "ix_new" ON "public"."users" USING hash ("a");`,
		`ALTER TABLE "public"."users" DROP CONSTRAINT "fk_old";`,
		`ADD CONSTRAINT "fk_new" FOREIGN KEY ("b") REFERENCES "public"."p" ("id") ON UPDATE RESTRICT;`,
		`COMMENT ON TABLE "public"."users" IS 'hi';`,
	}
	for _, w := range wantFragments {
		if !strings.Contains(joined, w) {
			t.Errorf("missing statement %q in:\n%s", w, joined)
		}
	}
}

